#pragma mark actionMD5算法方法
//MD5
@implementation NSString(action)
-(NSString *)MD5Long:(int)md5long
{
    NSString *restr=@"";
    const char *cStr = [self UTF8String];
    unsigned char result[md5long];
    CC_MD5(cStr, strlen(cStr), result);
    for (int x=0; x<md5long; x++) {
        restr=[restr stringByAppendingFormat:@"%02X",result[x]];
        
    }
    return restr;
}
-(NSString *)MD532
{
    return [self MD5Long:32];
}
-(NSString * )MD516
{
    return [self MD5Long:16];
}
@end


